    void listarEncomienda() {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        ControlEncomienda controlEncomienda = new ControlEncomienda();

        tblEncomiendas.setModel(modelo);
        modelo.addColumn("ID");
        modelo.addColumn("AGENCIA");
        modelo.addColumn("CLIENTE");
        modelo.addColumn("RECEPTOR");
        modelo.addColumn("FECHA");
        modelo.addColumn("PRECIO");
        modelo.addColumn("ESTADO");

        modelo.setRowCount(0);
        List<ListarEncomiendasDTO> lista = controlEncomienda.listarEncomiendasPersonalizado(idAgencia, fechaInicio, fechaFin, cliente, receptor);
        for (ListarEncomiendasDTO e : lista) {
            Object data[] = {e.getId(), e.getAgencia(), e.getCliente(), e.getReceptor(), e.getFecha(), e.getPrecio(), e.getEstado()};
            modelo.addRow(data);
        }
        Utilitario.ocultarColumnas(tblEncomiendas, 0);
    }

    void listarDetalleEncomienda(int idEncomienda) {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        ControlDetalleEncomienda controlDetalleEncomienda = new ControlDetalleEncomienda();

        tblDetalle.setModel(modelo);
        modelo.addColumn("CATEGORIA");
        modelo.addColumn("PRODUCTO");
        modelo.addColumn("PESO");
        modelo.addColumn("CANTIDAD");
        modelo.addColumn("PRECIO UNIT");
        modelo.addColumn("TOTAL");

        modelo.setRowCount(0);
        List<ListarDetalleEncomiendasDTO> lista = controlDetalleEncomienda.listarDetalleEncomiendaPersonalizado(idEncomienda);
        for (ListarDetalleEncomiendasDTO d : lista) {
            Object data[] = {d.getCategoria(), d.getProducto(), d.getPeso(), d.getCantidad(), d.getPrecioUnitario(), d.getTotal()};
            modelo.addRow(data);
        }
        tblDetalle.clearSelection();
    }

    void listarAgencias() {
        cboAgencia.removeAllItems();
        ControlAgencia controlAgencia = new ControlAgencia();
        List<Agencia> lista = controlAgencia.listar();
        cboAgencia.addItem("SELECCIONAR");
        for (Agencia agencia : lista) {
            agenciaMap.put(agencia.getId(), agencia.getDescripcion());
            cboAgencia.addItem(agencia.getDescripcion());
        }
    }

